                <h6 class="fw-bolder">What is Message Vault?</h6>
                <p>
                    Message Vault is an easy way to transmit messages and files whilst keeping them
                    clear of any chat logs or message trails that could be found later. It allows you to feel
                    confident that any sensitive information you wish to share isn't just sitting on a server
                    somewhere, or in a position that is accessible to anyone who may gain access to your devices.
                    Any data you host on Message Vault is deleted after the expiry time you provide elapses, or after
                    a secret is read - whatever happens first.
                </p>

                <br />
                
                <h6 class="fw-bolder">Is My Data Secure?</h6>
                <p>
                    You can rest assured any data you provide Message Vault - be it text-based secrets or files -
                    is stored securely and inaccessible by unauthorised users. The application has been created with
                    Laravel, an industry-leading and highly secure PHP Framework that receives regular security patches
                    to ensure it conforms with modern requirements and remains safe against any potential vulnerabilites.
                </p>

                <br />

                <h6 class="fw-bolder">Can I Trust Message Vault With My Data?</h6>
                <p>
                    Message Vault encrypts all data using the AES-256-CBC cipher, rendering it completely unreadable
                    to both us and anyone that, in the (highly) unlikely event of a breach, managed to access
                    the system's database or stored files. The 'key' that is required to decrypt the data is provided
                    to the user who generates the secret within the given URL, and isn't stored anywhere on our system;
                    therefore, we couldn't do anything malicious even if we wanted to!
                </p>

                <br />

                <h6 class="fw-bolder">Is Message Vault Open Source?</h6>
                <p>
                    Message Vault is completely open-source. You can find the source code
                    <a target="_blank" href="https://github.com/samharvey44/message-vault" rel="noopener noreferrer">here</a>
                </p>
